    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtId.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Doctor ID");
        }
        else if(txtName.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Doctor Name");
        }
        else if(txtT.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Time");
        }
        else if(txtC.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Charge");
        }
        else if(txtS.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Specialist");
        }
        else if(txtD.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Date");
        }
        else
        {
            try {
                //con = DriverManager.getConnection("jdbc:mysql://localhost:3306/healthcare_management","root","112233mysql");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/healthcare_management", "root","");
                PreparedStatement add = con.prepareStatement("insert into Appointment(DoctorID,Doctorname,Time,Date,Specialist,Charge)values(?,?,?,?,?,?)");
                add.setString(1, txtId.getText());
                add.setString(2, txtName.getText());
                
                add.setString(3, txtT.getText());
                add.setString(4, txtD.getText());
                add.setString(5, txtS.getText());
                
                add.setString(6, txtC.getText());
                add.executeUpdate();
                JOptionPane.showMessageDialog(this,"Data Saved");
                txtId.setText("");
                txtName.setText("");
                txtT.setText("");
                txtD.setText("");
                txtS.setText("");
                
                txtC.setText("");
              
                
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

