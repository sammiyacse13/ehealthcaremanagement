    private void btnSave2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave2ActionPerformed
        // TODO add your handling code here:
        if(txtIdP.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Patient ID");
        }
        else if(txtNameP.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Patient Name");
        }
        else if(txtTime.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Time");
        }
        else if(txtG.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Gender");
        }
        else if(txtA.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Age");
        }
        else if(txtD2.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Diesease");
        }
        else
        {
            try {
                //con = DriverManager.getConnection("jdbc:mysql://localhost:3306/healthcare_management","root","112233mysql");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/healthcare_management", "root", "");
                PreparedStatement add = con.prepareStatement("insert into Booking(PatientID,name,Age,Gender,Diesease,DoctorID,Doctorname,Time,Date)values(?,?,?,?,?,?,?,?,?)");
                add.setString(1, txtIdP.getText());
                add.setString(2, txtNameP.getText());
                
                add.setString(3, txtA.getText());
                add.setString(4, txtG.getText());
                add.setString(5, txtD2.getText());
                add.setString(6, txDocID.getText());
                add.setString(7, txDocN.getText());
                add.setString(8, txtTime.getText());
                add.setString(9, txtDate.getText());
                add.executeUpdate();
                JOptionPane.showMessageDialog(this,"Data Saved");
                txtIdP.setText("");
                txtNameP.setText("");
                txtA.setText("");
                txtG.setText("");
                txtD2.setText("");
                txDocID.setText("");        
                txDocN.setText("");      
                txtTime.setText("");
                txtDate.setText("");
                
                
              
                
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSave2ActionPerformed

